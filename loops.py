# Loop in Python - Basic Example
# -------------------------------

# Loops are used to repeat a block of code multiple times.

# Python provides:
# 1. for loop — used when we know how many times to loop
# 2. while loop — used when we loop until a condition is met

# -------------------------------
# Example: Print numbers from 1 to 5 using a for loop
# -------------------------------

# range(1, 6) generates numbers from 1 to 5 (6 is not included)
for i in range(1, 6):
    # i will take values 1, 2, 3, 4, 5 one by one
    # print the current value of i
    print(i)  
